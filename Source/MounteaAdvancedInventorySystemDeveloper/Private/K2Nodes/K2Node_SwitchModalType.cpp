void UK2Node_SwitchModalType::PostEditChangeProperty(struct FPropertyChangedEvent& PropertyChangedEvent)
{
	bool bIsDirty = false;
	FName propertyName = (PropertyChangedEvent.Property != NULL) ? PropertyChangedEvent.Property->GetFName() : NAME_None;
	if (propertyName == TEXT("PinNames"))
		bIsDirty = true;
	else if (propertyName == TEXT("bIsCaseSensitive"))
	{
		SetupCaseSensitivityFunction();
		FunctionClass = UKismetStringLibrary::StaticClass();
		bIsDirty = true;
	}

	if (bIsDirty)
		ReconstructNode();
	Super::PostEditChangeProperty(PropertyChangedEvent);
	GetGraph()->NotifyNodeChanged(this);
}

