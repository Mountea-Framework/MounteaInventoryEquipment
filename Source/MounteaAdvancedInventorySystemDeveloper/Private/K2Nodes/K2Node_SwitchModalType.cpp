UK2Node_SwitchModalType::UK2Node_SwitchModalType(const FObjectInitializer& ObjectInitializer)
	: Super(ObjectInitializer)
{
	bCanRenameNode = false;
	bHasDefaultPin = false;
	bIsCaseSensitive = false;
	SetupCaseSensitivityFunction();
	FunctionClass = UKismetStringLibrary::StaticClass();
}

void UK2Node_SwitchModalType::SetupCaseSensitivityFunction()
{
	FunctionName = (bIsCaseSensitive == true)
		? TEXT("NotEqual_StrStr")
		: TEXT("NotEqual_StriStri");
}

void UK2Node_SwitchModalType::CreateSelectionPin()
{
	UEdGraphPin* pin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_String, TEXT("Selection"));
	GetDefault<UEdGraphSchema_K2>()->SetPinAutogeneratedDefaultValueBasedOnType(pin);
}

FEdGraphPinType UK2Node_SwitchModalType::GetPinType() const
{
	FEdGraphPinType pinType;
	pinType.PinCategory = UEdGraphSchema_K2::PC_String;
	return pinType;
}

FName UK2Node_SwitchModalType::GetPinNameGivenIndex(const int32 Index) const
{
	check(Index);

	TArray<FName> visiblePins;
	GetVisiblePinNames(visiblePins);

	return visiblePins[Index];
}

void UK2Node_SwitchModalType::CreateCasePins()
{
	TArray<FName> predefinedModalTypes;
	GetModalTypesFromSettings(predefinedModalTypes);

	for (const FName& modalType : predefinedModalTypes)
	{
		if (!PinNames.Contains(modalType))
			PinNames.Add(modalType, false);
	}

	for (const TPair<FName, bool>& pinPair : PinNames)
	{
		if (pinPair.Value)
		{
			UEdGraphPin* newPin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, pinPair.Key);
			newPin->bAllowFriendlyName = false;
		}
	}
}

FName UK2Node_SwitchModalType::GetUniquePinName()
{
	FName newPinName;
	int32 pinIndex = 0;
	while (true)
	{
		newPinName = *FString::Printf(TEXT("Case_%d"), pinIndex++);
		if (!FindPin(newPinName))
			break;
	}
	return newPinName;
}

void UK2Node_SwitchModalType::AddPinToSwitchNode()
{
	const FName pinName = GetUniquePinName();
	PinNames.Add(pinName, false);

	UEdGraphPin* uniquePin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, pinName);
	uniquePin->bAllowFriendlyName = false;
}

void UK2Node_SwitchModalType::RemovePin(UEdGraphPin* TargetPin)
{
	checkSlow(TargetPin);

	if (IsPredefinedPin(TargetPin->PinName))
		return;

	PinNames.Remove(TargetPin->PinName);
}

void UK2Node_SwitchModalType::GetModalTypesFromSettings(TArray<FName>& OutModalTypes)
{
	OutModalTypes.Empty();

	const auto settings = GetDefault<UMounteaAdvancedInventorySettings>();
	if (!settings) return;

	const UMounteaAdvancedInventoryGlobalUIConfig* globalUIConfig = settings->GlobalUIConfig.LoadSynchronous();
	if (!globalUIConfig) return;

	TArray<FString> modalTypes = globalUIConfig->ModalTypes.Array();
	modalTypes.Sort();

	for (const FString& modalType : modalTypes)
	{
		OutModalTypes.Add(FName(*modalType));
	}
}

bool UK2Node_SwitchModalType::IsPredefinedPin(const FName& PinName)
{
	TArray<FName> predefinedModalTypes;
	GetModalTypesFromSettings(predefinedModalTypes);

	return predefinedModalTypes.Contains(PinName);
}

void UK2Node_SwitchModalType::GetVisiblePinNames(TArray<FName>& OutVisiblePins) const
{
	OutVisiblePins.Empty();

	for (const TPair<FName, bool>& pinPair : PinNames)
	{
		if (pinPair.Value)
			OutVisiblePins.Add(pinPair.Key);
	}
}

